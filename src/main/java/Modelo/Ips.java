
package Modelo;

public class Ips {
    private int id_ips;
    private String nombre;

    public Ips(int id_ips, String nombre) {
        this.id_ips = id_ips;
        this.nombre = nombre;
    }

    public int getId_ips() {
        return id_ips;
    }

    public String getNombre() {
        return nombre;
    }
}

